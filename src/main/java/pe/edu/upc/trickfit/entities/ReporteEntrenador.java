package pe.edu.upc.trickfit.entities;

public class ReporteEntrenador {
    private String nombre_usuario;
    private String nivel_rutina;
    private String numerorutinas;

    public String getNombre_usuario() {
        return nombre_usuario;
    }

    public void setNombre_usuario(String nombre_usuario) {
        this.nombre_usuario = nombre_usuario;
    }

    public String getNivel_rutina() {
        return nivel_rutina;
    }

    public void setNivel_rutina(String nivel_rutina) {
        this.nivel_rutina = nivel_rutina;
    }

    public String getNumerorutinas() {
        return numerorutinas;
    }

    public void setNumerorutinas(String numerorutinas) {
        this.numerorutinas = numerorutinas;
    }



}
