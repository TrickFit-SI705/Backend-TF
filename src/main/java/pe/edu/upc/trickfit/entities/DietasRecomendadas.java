package pe.edu.upc.trickfit.entities;

public class DietasRecomendadas {
    private String tipo_usuario;
    private String nombre_usuario;
    private String titulo_dieta;
    private String recomendaciones;

    public String getTipo_usuario() {
        return tipo_usuario;
    }

    public void setTipo_usuario(String tipo_usuario) {
        this.tipo_usuario = tipo_usuario;
    }

    public String getNombre_usuario() {
        return nombre_usuario;
    }

    public void setNombre_usuario(String nombre_usuario) {
        this.nombre_usuario = nombre_usuario;
    }

    public String getTitulo_dieta() {
        return titulo_dieta;
    }

    public void setTitulo_dieta(String titulo_dieta) {
        this.titulo_dieta = titulo_dieta;
    }

    public String getRecomendaciones() {
        return recomendaciones;
    }

    public void setRecomendaciones(String recomendaciones) {
        this.recomendaciones = recomendaciones;
    }



}
